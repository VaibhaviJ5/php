3. Data Types
PHP supports several data types including strings, integers, floats, booleans, arrays, and objects.

Example:


<?php
    $string = "Hello, PHP!";
    $integer = 42;
    $float = 3.14;
    $boolean = true;

    echo $string . "<br>";
    echo $integer . "<br>";
    echo $float . "<br>";
    echo $boolean ? "True" : "False";
?>
Explanation: This example shows different data types. 
The ? : operator is used to convert the boolean value to a string representation.