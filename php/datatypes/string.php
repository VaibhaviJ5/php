<!-- 1. String
A string is a sequence of characters enclosed in quotes. 
PHP supports both single and double quotes for strings. -->

Examples:


<?php
    $string1 = "Hello, World!"; // Double quotes
    $string2 = 'PHP is fun!';    // Single quotes

    echo $string1 . "<br>";
    echo $string2;
?>


Explanation: Both single and double quotes are used to create strings,
 but double quotes allow for variable interpolation.